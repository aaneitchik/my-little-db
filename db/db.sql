--  DDL for Trigger ACTORS_OCCUPATION_CHECK
--------------------------------------------------------

  CREATE OR REPLACE TRIGGER "ROOT"."ACTORS_OCCUPATION_CHECK" 
BEFORE INSERT ON ACTORS_IN_FILM
FOR EACH ROW
DECLARE
cnt NUMBER;
yr NUMBER;
BEGIN
  SELECT FILMS.YEAR INTO yr FROM FILMS
  WHERE FILMS.ID = :NEW.FILM_ID;
  
  SELECT COUNT(*) into cnt from (
  SELECT f.YEAR FROM FILMS f
  INNER JOIN ACTORS_IN_FILM a
  ON f.ID = a.FILM_ID
  WHERE f.YEAR = yr
  AND a.ACTOR_ID = :NEW.ACTOR_ID
  ) C;
  IF cnt >= 2 THEN
  RAISE_APPLICATION_ERROR(-20001, 'Too many films for one actor in year!');
  ROLLBACK;
  END IF;
END;
/
ALTER TRIGGER "ROOT"."ACTORS_OCCUPATION_CHECK" ENABLE;
